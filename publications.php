<h3>Theses</h3>

<ol>
  <li>
	<p class="title"><a href="publications/sdstrowes-phd-thesis.pdf">Compact
	Routing for the Future Internet</a></p>
	<p class="author">S.D. Strowes</p>

	<ul>

	  <li>PhD thesis, defended 2012.</li>

	  <li>Abstract:

		<p><em>
			The Internet relies on its inter-domain routing system
			to allow data transfer between any two endpoints
			regardless of where they are located. This routing system
			currently uses a shortest path routing algorithm (modified
			by local policy constraints) called the Border Gateway
			Protocol. The massive growth of the Internet has led to
			large routing tables that will continue to grow. This will
			present a serious engineering challenge for router
			designers in the long-term, rendering state (routing
			table) growth at this pace unsustainable.
		</em></p>

		<p><em>
			There are various short-term engineering solutions
			that may slow the growth of the inter-domain routing
			tables, at the expense of increasing the complexity of the
			network. In addition, some of these require manual
			configuration, or introduce additional points of failure
			within the network. These solutions may give an
			incremental, constant factor, improvement. However, we
			know from previous work that all shortest path routing
			algorithms require forwarding state that grows linearly
			with the size of the network in the worst case.
		</em></p>

		<p><em>
			Rather than attempt to sustain inter-domain routing
			through a shortest path routing algorithm, compact
			routing algorithms exist that guarantee worst-case
			sub-linear state requirements at all nodes by allowing an
			upper-bound on path length relative to the theoretical
			shortest path, known as path stretch. Previous work
			has shown the promise of these algorithms when applied to
			synthetic graphs with similar properties to the known
			Internet graph, but they haven't been studied in-depth on
			Internet topologies derived from real data.
		</em></p>


		<p><em>
			In this dissertation, I demonstrate the consistently
			strong performance of these compact routing algorithms for
			inter-domain routing by performing a longitudinal study of
			two compact routing algorithms on the Internet Autonomous
			System (AS) graph over time.  I then show, using the
			k-cores graph decomposition algorithm, that the
			structurally important nodes in the AS graph are highly
			stable over time. This property makes these nodes suitable
			for use as the &ldquo;landmark&rdquo; nodes used by the most stable
			of the compact routing algorithms evaluated, and the use
			of these nodes shows similar strong routing performance.
			Finally, I present a decentralised compact routing
			algorithm for dynamic graphs, and present state
			requirements and message overheads on AS graphs using
			realistic simulation inputs.
		</em></p>

		<p><em>
		  To allow the continued long-term growth of Internet routing
		  state, an alternative routing architecture may be
		  required. The use of the compact routing algorithms
		  presented in this dissertation offer promise for a scalable
		  future Internet routing system.
		</em></p>

	  </li>


	</ul>
  </li>

  <li>
	<p class="title"><a href="publications/sdstrowes-msci-dissertation.pdf">Peer-to-Peer
Audio Conferencing</a></p>
	<p class="author">S.D. Strowes</p>

	<ul>

	  <li>Masters dissertation, submitted 2005.</li>

	  <li>Abstract: <p><em>The intention of IP Multicast as a service
			provided by network infrastructure was to allow groups of
			hosts to share similar data, leaving the network to deal
			with the complexities of group membership and routing
			issues. One natural use for IP Multicast was group
			conferencing.</em></p>

		<p><em>Adoption of IP Multicast has not been swift, however,
			leaving conferencing applications designed for use with
			the service unusable over significant parts of the
			Internet.</em></p>

		<p><em>This dissertation presents Orta, a new peer-to-peer
			network overlay which is designed to allow group
			conferencing. The implementation is presented as a
			reusable software library, and is not tied to any existing
			application; one application, the Robust Audio Tool, is
			modified to use this library rather than IP Multicast as a
			proof-of-concept implementation. Presented are
			implementation details and evaluation results detailing
			the characteristics of the overlay, with some focus on its
			usefulness for real-time applications.</em></p>
	  </li>

	</ul>

  </li>
